#include "../../libft.h"

int	ft_strlen_comp(char **s)
{
	int i;
	int j;

	i = 0;
	j = 0;
	while (s[i] && s[(i + 1)])
	{
		j = ft_strlen(s[i]);
		i++;
		if (j != ft_strlen(s[i]))
			return (0);
	}
	return (1);
}