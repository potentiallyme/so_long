#include "../../libft.h"

t_list	*ft_lstmap(t_list *lst, long (*f)(long), void (*del)(long))
{
	t_list	*new_list;
	t_list	*new_node;
	long	set;

	if (!lst || !f || !del)
		return (NULL);
	new_list = NULL;
	while (lst)
	{
		set = f(lst->content);
		new_node = ft_lstnew(set);
		if (!new_node)
		{
			del(set);
			ft_lstclear(&new_list, (*del));
			return (new_list);
		}
		ft_lstadd_back(&new_list, new_node);
		lst = lst->next;
	}
	return (new_list);
}
