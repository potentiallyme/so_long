#include "../../libft.h"

int	ft_make_rand(int i, int j)
{
	int	r;

	if (i == 0 && j == 0)
		return (1);
	if (i == 0 && j != 0)
		return (j * 147 / 100 % 10);
	if (i != 0 && j == 0)
		return (i * 147 / 100 % 10);
	r = ((i * 147) % j);
	if (r >= 100)
		r = r % i / j;
	if (r >= 100)
		r = r / 100;
	r = r % 10;
	return (r);
}
